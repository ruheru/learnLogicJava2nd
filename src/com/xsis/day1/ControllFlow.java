package com.xsis.day1;

public class ControllFlow {

	public static void main(String[] args) {
		int n = 10;
		if (n==13){
			System.out.println("cetak 1");
		}
		else if (n == 10){
			System.out.println("cetak 2");
		}
		else if (n > 1){
			System.out.println("cetak 3");
		}
		else{
			System.out.println("cetak 4");
		}
	}
}
